export const currentSolarTerm = () => {
  const now = new Date()
  const month = now.getMonth() + 1
  const day = now.getDate()
  // Simplified mapping
  if ((month === 2 && day >= 3) || month === 3 || (month === 4 && day < 20)) return solarTerms[2] // spring
  if ((month === 4 && day >= 20) || month === 5 || month === 6 || (month === 7 && day < 22)) return solarTerms[6] // summer
  if ((month === 7 && day >= 22) || month === 8 || month === 9 || (month === 10 && day < 23)) return solarTerms[12] // autumn
  return solarTerms[18] // winter
}
